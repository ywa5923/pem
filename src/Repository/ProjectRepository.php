    public function getWithSearchQueryBuilder(?string $searchTerm):QueryBuilder
    {
        $qb = $this->createQueryBuilder('p')
            ->innerJoin('p.projectUsers','pu')
            ->innerJoin('pu.user','u')
            ->addSelect('pu')
            ->addSelect('u');


        if ($searchTerm) {

            $qb->andWhere('p.title LIKE :term or p.contract like :term OR u.firstName like :term or u.lastName like :term')
                ->setParameter('term', '%' . $searchTerm . '%')
            ;
        }



        return $qb->orderBy('p.createdAt', 'DESC');


    }
